namespace Chapter_7___Important_Keywords
{
    public class VarAndDynamic
    {
        static void Main(string[] args)
        {
            // var
            var a = 10;
            a = "Interview";

            Console.WriteLine(a);

            // dynamic
            dynamic b = 10;
            b = "Interview";

            Console.WriteLine(b);

        }
    }
}
